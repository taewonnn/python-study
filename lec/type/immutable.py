# mutable objects 가변 객체
# - 다른 불변/가변 객체들이 저장될 수 있는 집
# - 집 안 가구를 바꾼다고 집 주소가 바뀌지 않음


# 리스트
list_1 = [1, 2, 3, 4, 5]
list_1_id_a = id(list_1) # 🔴

# 다른 변수에 값을 할당
# 집의 주소를 복사해서 주는 것과 같음
# 즉 같은 집의 주소를 갖고 있는 것
list_2 = list_1
list_2_id = id(list_2)

# 리스트 안의 요소 변경
